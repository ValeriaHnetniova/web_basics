function printPattern() {
    let pattern = ""; // Створюємо порожній рядок для накопичення результату

    // Масив із кількістю зірочок для кожного рядка
    const stars = [9, 17, 23, 27, 29, 29, 27, 23, 17, 9];

    // Проходимо по кожному елементу масиву і створюємо рядок із зірочками відповідної довжини
    for (let i = 0; i < stars.length; i++) {
        pattern += "*".repeat(stars[i]) + "\n"; // Додаємо потрібну кількість зірочок і перехід на новий рядок
    }

    console.log(pattern); // Виводимо результат у консоль
}
printPattern();
console.log(printPattern);


